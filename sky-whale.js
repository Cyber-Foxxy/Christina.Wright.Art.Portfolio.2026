let activeCategory = "All";
let visibleItems = [...skyWhaleItems];
let currentIndex = 0;

const categories = ["All", ...new Set(skyWhaleItems.map(item => item.category))];

function renderFilters() {
  filterButtons.innerHTML = categories.map(category => `
    <button class="filter-btn ${category === activeCategory ? "active" : ""}" type="button" data-category="${category}">
      ${category}
    </button>
  `).join("");

  document.querySelectorAll(".filter-btn").forEach(button => {
    button.addEventListener("click", () => {
      activeCategory = button.dataset.category;
      renderFilters();
      renderGallery();
    });
  });
}

function renderGallery() {
  visibleItems = activeCategory === "All"
    ? [...skyWhaleItems]
    : skyWhaleItems.filter(item => item.category === activeCategory);

  gallery.innerHTML = visibleItems.map((item, index) => `
    <article class="art-card" tabindex="0" data-index="${index}">
      <img src="${item.src}" alt="${item.title}" loading="lazy" />
      <div class="card-text">
        <span class="category-pill">${item.category}</span>
        <h3>${item.title}</h3>
        <p>${item.description}</p>
      </div>
    </article>
  `).join("");

  document.querySelectorAll(".art-card").forEach(card => {
    card.addEventListener("click", () => openLightbox(Number(card.dataset.index)));
    card.addEventListener("keydown", event => {
      if (event.key === "Enter" || event.key === " ") {
        event.preventDefault();
        openLightbox(Number(card.dataset.index));
      }
    });
  });
}

function openLightbox(index) {
  currentIndex = index;
  const item = visibleItems[currentIndex];
  if (!item) return;
  lightboxImg.src = item.src;
  lightboxImg.alt = item.title;
  lightboxCaption.textContent = `${item.title} — ${item.category}`;
  lightbox.classList.add("open");
  lightbox.setAttribute("aria-hidden", "false");
}

function closeLightbox() {
  lightbox.classList.remove("open");
  lightbox.setAttribute("aria-hidden", "true");
  lightboxImg.src = "";
}

function showLightbox(step) {
  if (!visibleItems.length) return;
  currentIndex = (currentIndex + step + visibleItems.length) % visibleItems.length;
  openLightbox(currentIndex);
}

closeBtn?.addEventListener("click", closeLightbox);
prevBtn?.addEventListener("click", () => showLightbox(-1));
nextBtn?.addEventListener("click", () => showLightbox(1));
lightbox?.addEventListener("click", event => {
  if (event.target === lightbox) closeLightbox();
});
document.addEventListener("keydown", event => {
  if (!lightbox.classList.contains("open")) return;
  if (event.key === "Escape") closeLightbox();
  if (event.key === "ArrowLeft") showLightbox(-1);
  if (event.key === "ArrowRight") showLightbox(1);
});

// Music button: browser rules require the visitor to click Play once.
// The time and play/pause state are saved so it resumes close to the same spot on the next page.
const music = document.getElementById("siteMusic");
const musicToggle = document.getElementById("musicToggle");
if (music && musicToggle) {
  music.volume = 0.16;

  const savedTime = parseFloat(localStorage.getItem("musicTime") || "0");
  const savedState = localStorage.getItem("musicPlaying") === "true";

  if (!Number.isNaN(savedTime) && savedTime > 0) {
    music.addEventListener("loadedmetadata", () => {
      if (savedTime < music.duration) music.currentTime = savedTime;
    }, { once: true });
  }

  musicToggle.textContent = savedState ? "♫ Resume Music" : "♫ Play Music";

  musicToggle.addEventListener("click", async () => {
    try {
      if (music.paused) {
        await music.play();
        localStorage.setItem("musicPlaying", "true");
        musicToggle.textContent = "♫ Pause Music";
      } else {
        music.pause();
        localStorage.setItem("musicPlaying", "false");
        musicToggle.textContent = "♫ Play Music";
      }
    } catch (error) {
      musicToggle.textContent = "♫ Click Again";
    }
  });

  music.addEventListener("play", () => {
    localStorage.setItem("musicPlaying", "true");
    musicToggle.textContent = "♫ Pause Music";
  });

  music.addEventListener("pause", () => {
    if (!music.ended) {
      localStorage.setItem("musicPlaying", "false");
      musicToggle.textContent = "♫ Play Music";
    }
  });

  setInterval(() => {
    if (!Number.isNaN(music.currentTime)) {
      localStorage.setItem("musicTime", music.currentTime.toString());
    }
  }, 700);
}

